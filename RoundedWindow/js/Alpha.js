
function get(value) {

    switch(value) {
    case 0: return "00"
    case 10: return "1A"
    case 20: return "33"
    case 30: return "4D"
    case 40: return "66"
    case 50: return "80"
    case 60: return "99"
    case 70: return "B3"
    case 80: return "CC"
    case 90: return "E6"
    case 100: return "FF"

    default: return "FF"

    }

}
